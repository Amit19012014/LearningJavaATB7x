package ex_13072024;

public class Lab034 {
    public static void main(String[] args) {

        //Escap characters
        char c = '\n'; // New Line / next Line
        char c1 = '\t'; //Add Tab
        char c2 = '\b';// Backlash , delete 1 char
        char c3 = '\r'; // Backlash , delete 1 word
        System.out.println("Amit" + c2 + "Singh");
        System.out.println("Amit" + c3 + "Singh");
        System.out.println("Amit" + c1 + "Singh");
        System.out.println("Amit" + c + "Singh");

    }
}
